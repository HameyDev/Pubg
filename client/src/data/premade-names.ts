export function searchNames(query: string, names: PremadeName[]): PremadeName[] {
  if (!query.trim()) return names;
  
  const searchTerm = query.toLowerCase();
  return names.filter(name => 
    name.text.toLowerCase().includes(searchTerm) ||
    name.category.toLowerCase().includes(searchTerm) ||
    name.tags.some(tag => tag.toLowerCase().includes(searchTerm))
  );
}

export function filterByCategory(category: string, names: PremadeName[]): PremadeName[] {
  if (category === 'all') return names;
  return names.filter(name => name.category === category);
}

